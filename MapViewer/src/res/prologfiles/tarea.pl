irDesdeHacia(A, B) :-
    A\=B,
    puntoCalle(A, C, D),
    puntoCalle(B, C, D).

ir_hacia(X, Y) :-
    abolish(eslabon_prov, 1),
    abolish(eslabon, 1),
    assert(eslabon_prov(Y)),
    ir_hacia_rec(X, Y),
    assert(eslabon(Y)),
    writeln(Y).

ir_hacia_rec(X, Y) :-
    irDesdeHacia(X, Y),
    writeln(X),
    assert(eslabon(X)),
    assert(eslabon_prov(X)),
    !.
ir_hacia_rec(_, Y) :-
    irDesdeHacia(X, Y),
    eslabon_prov(X),
    fail.
ir_hacia_rec(X, Y) :-
    irDesdeHacia(Z, Y),
    not(eslabon_prov(Z)),
    assert(eslabon_prov(Z)),
    ir_hacia_rec(X, Z),
    writeln(Z),
    assert(eslabon(Z)),
    !.

